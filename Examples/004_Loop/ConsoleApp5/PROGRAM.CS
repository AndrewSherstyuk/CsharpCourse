using System;

/// <summary>
/// Факториал числа
/// Факториа́л — функция, определённая на множестве неотрицательных целых чисел. 
/// Название происходит от лат. factorialis — действующий, производящий, умножающий; 
/// обозначается n!, произносится эн факториа́л. 
/// Факториал натурального числа n определяется как произведение всех натуральных чисел от 1 до n включительно:
/// n! = 1 * 2 * 3 * ... * n
/// 5 ! = 1 * 2 * 3 * 4 * 5 = 120.
/// 4! = 1 * 2 * 3 * 4 = 24

/// </summary>
namespace Loop
{
    class Program
    {
        static void Main()
        {
            //Факториал данного числа определить
            int counter = 5;
            //Переменная для хранения результата 
            int factorial = 1;

            Console.Write("Факториал числа: {0}! = ", counter);

            do
            {
                // Сначала умножение, потом декремент. (Операция присвоения)
                factorial *= counter--;

                //Можно так (без операции присвоения)
                //factorial = factorial * counter --;

                //Можно так (без операции присвоения и дикримента)
                /*factorial = factorial * counter;
                counter = counter - 1;*/

            }
            //Условие
            while (counter > 0);

            //Вывод результата выполнения операции на экран
            Console.WriteLine("{0}", factorial);

            //Задержка
            Console.ReadKey();
        }
    }
}
